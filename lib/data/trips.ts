/** Kinds of stop on a trip itinerary. */
export type TripStopKind = "acquire" | "hook" | "pickup" | "drop-off";

export type TripStop = {
  id: string;
  kind: TripStopKind;
  /** Facility / shipper name, e.g. "Yard Goreway". */
  name: string;
  /** City label, e.g. "Mississauga, ON". */
  location: string;
  /** Full street address. */
  address: string;
  /** Latitude — used to place the map pin. */
  lat: number;
  /** Longitude — used to place the map pin. */
  lng: number;
  /** Appointment date & time, display string. */
  appointment: string;
  /** Pick-up / drop-off reference number, when relevant. */
  pickupNumber: string | null;
  /** Temperature requirement, e.g. "28°F". */
  temperature: string | null;
  /** Contact phone, when relevant. */
  phone: string | null;
  email: string;
  /** Power unit number, when relevant. */
  powerUnit: string | null;
  /** Trailer number, when relevant. */
  trailer: string | null;
  /** Per-stop dispatch note (pickup note / drop-off note). */
  note: string | null;
  completed: boolean;
};

export type Trip = {
  id: string;
  /** Origin city label. */
  origin: string;
  /** Destination city label. */
  destination: string;
  /** Trip start date & time, display string. */
  startDate: string;
  /** Trip end date & time, display string. */
  endDate: string;
  /** Countdown pill text, e.g. "IN 3d 6hr" — null for completed trips. */
  countdown: string | null;
  leadDriver: string;
  teamDriver: string;
  dispatchedBy: string;
  issuedOn: string;
  /** Equipment type, e.g. "RF" (reefer) / "DV" (dry van). */
  equipment: string;
  powerUnit: string;
  trailer: string;
  /** Trip-level dispatch note. */
  note: string | null;
  stops: TripStop[];
};

